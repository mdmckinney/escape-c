/**********************************************************************
*  CLEAN RAW INPUT (REMOVE "\n" FROM STRING & MAKE STRING LOWER CASE) *
**********************************************************************/
char *CleanRawInput(char *rawinput, int len)
{
    char *input = malloc(sizeof(char) * len);

	// CONVERTING INPUT TO LOWER CASE
    for (int i = 0; i < len; ++i) {
        input[i] = tolower((unsigned char)rawinput[i]);
    }

	// STRIPPING "\n" OUT OF STRING BY REPLACING IT WITH \0.
	input[strcspn (input, "\n")] = '\0';

    return input;
}

