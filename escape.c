/**********************************************************************
*                       SAVING THE GAME TO FILE                       *
**********************************************************************/
void saveGame(){

	for(;;) {

		clearscreen();
		
		printf("\t\t-----------------------------------------------");
		printf("\n\t\t\t\t      SAVE\n");
		printf("\t\t-----------------------------------------------");			

		printf("\n\nPlease enter the name of the save file.");
		printf("\n\n>");

		// GET PLAYER'S INPUT AND CLEAN IT
		fgets(rawinput, sizeof(rawinput), stdin);
		char *input = CleanRawInput(rawinput, strlen(rawinput));
		
		// USING ".escape" AS FILE EXTENSION FOR THE SAVE FILE
		char *fileext = ".escape";		

		// CONCENTATING PLAYER'S INPUT WITH FILE EXTENSION TO GET FULL FILE NAME
		strcat(input, fileext);

		FILE *savefile;

		// OPEN/CREATE THE SAVE FILE
		savefile = fopen(input, "w");

		// SAVING GAME STATES TO SAVE FILE
		fprintf(savefile, "%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d%d", roomid, 
				northdoor, southdoor, eastdoor, westdoor, bronzebox, northaccesspanel, 
				steelbox, southaccesspanel, deadplant, planter, flashlighton, 
				flashlightbatteries, titaniumbox, tungstengate, escaped, 
				paper, brasskey, aluminumkey, ironkey, flashlight, batteries);	

		// CLOSING THE SAVE FILE
		fclose (savefile);

		printf("\nGame saved to %s.\n", input);
		printf("\nPress enter to return to the game.");

		getchar();

		if (roomid == 0) {
			opening();
		}

		if (roomid == 1) {
			centerRoom();
		}

		if (roomid == 2) {
			eastRoom();
		}	

		if (roomid == 3) {
			westRoom();
		}

		if (roomid == 4) {
			southRoom();
		}			

		if (roomid == 5) {
			northRoom();
		}	

		if (roomid == 6) {
			endgame();
		}
	}

}

